    private void ThemChuHoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemChuHoBtnActionPerformed
        // TODO add your handling code here:
        String HOTEN = tenChuHo.getText();
        String SDT = soDienThoai.getText();
        String CCCD = cccd.getText();
        String GIOITINH = String.valueOf(jComboBoxGioiTinh.getSelectedItem());
        //String MAPQ;
        String MAPQ = "R3";
        String EMAIL = email.getText();
        String DIACHITT = diachitt.getText();
        
        Date currentDate = new Date(System.currentTimeMillis());
        SimpleDateFormat dateFormat1 = new SimpleDateFormat("yyyyMMddHHmmss");
        String dateString = dateFormat1.format(currentDate);
        String MACH = "CH" + dateString;

        if (HOTEN.isEmpty() || SDT.isEmpty() || DIACHITT.isEmpty() || CCCD.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Trường dữ liệu không được để trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ChuHoModel chuho = new ChuHoModel(MACH, HOTEN, SDT, CCCD, DIACHITT, GIOITINH);
                TaiKhoanModel taikhoan = new TaiKhoanModel(MACH, EMAIL, SDT, MAPQ );
                
                QuanLiChuHoCtrl.themChuHo(chuho, taikhoan);

                JOptionPane.showMessageDialog(null, "Tạo chủ hộ thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                // Đóng cửa sổ hiện tại
                Window window = SwingUtilities.getWindowAncestor((Component)evt.getSource());
                window.dispose();

            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Xảy ra lỗi khi tạo chủ hộ", "Lỗi", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_ThemChuHoBtnActionPerformed

