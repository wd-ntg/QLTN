    private void btnThemLoaiNuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLoaiNuocActionPerformed
        // TODO add your handling code here:
        String tenLoaiNuoc = txtTenLoaiNuoc.getText();
        String phiTreStr = txtPhiTre.getText();
        if (tenLoaiNuoc.isEmpty() || phiTreStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            if (txtMaLoaiNuoc.getText().isEmpty()) {
                try {
                    int phiTre = Integer.parseInt(phiTreStr);
                    WaterCategoryModel loai = new WaterCategoryModel(tenLoaiNuoc, phiTre);
                    WaterCategoryCtrl.themLoaiNuoc(loai);
                    lamMoi();
                    hienThiCacLoaiNuoc();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddWaterCategory1.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Loại nước đã tồn tại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemLoaiNuocActionPerformed

    private void btnThemDinhMucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDinhMucActionPerformed
        // TODO add your handling code here:
        String maLoai = txtMaLoai.getText();
        String tenLoaiNuoc = txtTenLoaiNuoc.getText();
        String chiSoDauStr = txtChiSoDau.getText();
        String chiSoSauStr = txtChiSoSau.getText();
        String donGiaStr = txtDonGia.getText();
        String thueStr = txtThue.getText();
        if (maLoai.isEmpty() || tenLoaiNuoc.isEmpty() || chiSoDauStr.isEmpty() || chiSoSauStr.isEmpty() || donGiaStr.isEmpty() || thueStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            if (txtMaDinhMuc.getText().isEmpty()) {
                try {
                    int chiSoDau = Integer.parseInt(chiSoDauStr);
                    int chiSoSau = Integer.parseInt(chiSoSauStr);
                    int donGia = Integer.parseInt(donGiaStr);
                    int thue = Integer.parseInt(thueStr);
                    if (chiSoDau < 0 || chiSoDau < 0 || donGia < 0 || thue < 0) {
                        JOptionPane.showMessageDialog(this, "Vui lòng nhập các số liệu là số lớn hơn 0", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } else if (chiSoDau > chiSoSau) {
                        JOptionPane.showMessageDialog(this, "Chỉ số sau phải lớn hơn chỉ số đầu", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } else {
                        ConsumptionQuotaModel dinhMuc = new ConsumptionQuotaModel(maLoai, tenLoaiNuoc, chiSoDau, chiSoSau, thue, donGia);
                        WaterCategoryCtrl.themDinhMuc(dinhMuc);
                        lamMoi();
                        hienThiDinhMuc(maLoai);
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddWaterCategory1.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Định mức nước đã tồn tại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemDinhMucActionPerformed

    private void tblLoaiNuocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaiNuocMouseClicked
        // TODO add your handling code here:
        lamMoi();
        btnThemLoaiNuoc.setEnabled(false);
        int selectedIndex = tblLoaiNuoc.getSelectedRow();
        if (selectedIndex >= 0) {
            WaterCategoryModel loai = dsLoaiNuoc.get(selectedIndex);
            txtMaLoaiNuoc.setText(loai.getMaLoai());
            txtTenLoaiNuoc.setText(loai.getTenLoai());
            txtPhiTre.setText(String.valueOf(loai.getPhiTre()));
            txtMaLoai.setText(loai.getMaLoai());
            txtTenLoai.setText(loai.getTenLoai());
            try {
                hienThiDinhMuc(loai.getMaLoai());
                int chiSoCuoi = WaterCategoryCtrl.hienThiChiSoCuoiLonNhat(loai.getMaLoai());
                txtChiSoDau.setText(String.valueOf(chiSoCuoi));
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddWaterCategory1.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("Chưa có dòng nào được chọn");
        }
    }//GEN-LAST:event_tblLoaiNuocMouseClicked

